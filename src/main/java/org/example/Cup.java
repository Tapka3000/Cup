package org.example;

public class Cup {
    public static void main(String[] args) {
        int cup = 200;
        double percent = 0.5;
        double half = cup * percent;

        System.out.println("Объем стакана " + cup + " мл");
        System.out.println("Стакан заполнен на " + half + " мл");
    }
}
